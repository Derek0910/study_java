package ch01;

public class _06_VariableEx {
	public static void main(String[] InCoDeCo) {
		char ch1 = 'z';
		System.out.println(ch1);
		System.out.println((int)ch1);
		
		char ch2 = 38;
		System.out.println(ch2);
		
		int int1 = 32; //아스키코드 32번 공백;
		System.out.println(int1);
		System.out.println((char)int1);
	}
}

